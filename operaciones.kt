package ejercicios

import kotlin.math.*

fun main () {
    var num1 = 5.0
    var num2 = 10.0
    var suma = num1 + num2
    var resta = num1 - num2
    var multiplicacion = num1 * num2
    var division = num1 / num2
    var exponente = 2.0
    var potencia = num1.pow(exponente)

    println("Suma: $suma")
    println("Resta: $resta")
    println("Multiplicación: $multiplicacion")
    println("División: $division")
    println("Potencia: $potencia")
}